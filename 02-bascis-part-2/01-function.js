// 🔹 What is a Basic Function in JavaScript?

// 👉 A function is a block of code that does a specific task.
// 👉 A basic function means a simple function jo:

// declare hota hai function keyword se

// input (parameters) le sakta hai

// output (return value) de sakta hai

// aur usko call karna padta hai chalane ke liye



// ---

// 🔹 Syntax (basic form)

// function functionName(parameter1, parameter2) {
//   // code to execute
//   return result;   // optional
// }


// ---

// 🔹 Example: Add Two Numbers

// function addNumbers(a, b) {
//   const sum = a + b;         // add the numbers
//   return sum;                // return the result
// }

// // calling the function
// const result = addNumbers(5, 7);
// console.log(result);  // Output: 12


// ---

// 🔍 Breakdown:

// 1. function addNumbers(a, b) → function declaration with 2 parameters (a, b).


// 2. const sum = a + b; → function ke andar calculation ho rahi hai.


// 3. return sum; → function value wapas kar raha hai.


// 4. addNumbers(5, 7) → function ko call kiya with arguments.


// 5. console.log(result) → output screen par dikhaya.




