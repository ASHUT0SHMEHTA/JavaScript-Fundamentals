// 🔹 Function Anatomy (Parts of a Function)

// Example Code:

// function greet(name) {
//   return Hello, ${name}!;
// }

// Breakdown (Anatomy):

// 1. function keyword
// → JavaScript ko batata hai ki tum ek function bana rahe ho.


// 2. Function Name (greet)
// → Function ka unique naam (isse call karoge).
// → Example: greet("Ashu").


// 3. Parameters (name)
// → Inputs jo function receive karta hai.
// → Multiple ho sakte hain: (a, b, c)


// 4. Curly Braces { ... }
// → Function ke andar ka code block (instructions).


// 5. Return Statement (return)
// → Function se output wapas dene ke liye.
// → Agar return nahi doge, by default function undefined return karega.




// ---

// 🔹 Call Site (Function Call)

// Function banane ke baad use call karna padta hai:

// console.log(greet("Ashutosh"));
// // Output: Hello, Ashutosh!

// 👉 Yaha "Ashutosh" argument hai jo parameter name ko milega.


// ---

// ✅ Summary of Function Anatomy:

// Keyword → function

// Name → function ka naam

// Parameters → input values (optional)

// Body → { code inside }

// Return → output (optional)

// Call → function ko chalana



