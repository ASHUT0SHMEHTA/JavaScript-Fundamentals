// Comparison Operators

// 5. > (Greater than)

// Checks if left value is greater than right value.

let score = 90;
console.log(score > 75); // true → 90 is greater than 75

// 6. < (Less than)

// Checks if left value is less than right value.

let temperature = 15;
console.log(temperature < 20); // true → 15 is less than 20

// 7. >= (Greater than or Equal to)

// Checks if left value is greater than or equal to right value.

let salary = 30000;
console.log(salary >= 30000);
// true → equal to 30000

// 8. <= (Less than or Equal to)

// Checks if left value is less than or equal to right value.

let speed = 60;
console.log(speed <= 80); // true → 60 is less than 80
