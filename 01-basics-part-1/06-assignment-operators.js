// 🖊 Assignment Operators in JavaScript

// 1. = (Simple Assignment)

// Value assign karta hai.

// let x = 10;   // x = 10


// ---

//     2. += (Add and Assign)

// Right side ki value ko left side ke variable ke saath add karke wapas assign karta hai.

// let x = 5;
// x += 3;   // x = x + 3
// console.log(x);  // 8


// ---

//     3. -= (Subtract and Assign)

// Subtract karke assign karta hai.

// let x = 10;
// x -= 4;   // x = x - 4
// console.log(x);  // 6


// ---

//     4. *= (Multiply and Assign)

// Multiply karke assign karta hai.

// let x = 6;
// x *= 2;   // x = x * 2
// console.log(x);  // 12


// ---

//     5. /= (Divide and Assign)

// Divide karke assign karta hai.

// let x = 20;
// x /= 5;   // x = x / 5
// console.log(x);  // 4


// ---

//     6. %= (Modulus and Assign)

// Remainder karke assign karta hai.

// let x = 10;
// x %= 4;   // x = x % 4
// console.log(x);  // 2


// ---

//     7. **= (Exponentiation and Assign)

// Power karke assign karta hai.

// let x = 2;
// x = 3;   // x = x  3
// console.log(x);  // 8


// ---

// ✅ Summary Table

// Operator Example Meaning Result

