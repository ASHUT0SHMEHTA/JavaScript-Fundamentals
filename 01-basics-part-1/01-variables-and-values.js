// Variables & Values

// 1. Variable

// A variable is a named storage (container) in memory.
// It is used to store data (like numbers, text, etc.).
// The value inside it can change anytime.

// 2. Value

// A value is the actual data stored inside the variable.
// Example: number (10), text ("Hello"), boolean (true/false).

// 3. Relationship

// Variable = container/jar.
// Value = content inside (cookies).

// 4. Example (JS)

let age = 20;  // "age" = variable, 20 = value
age = 25;      // value changed