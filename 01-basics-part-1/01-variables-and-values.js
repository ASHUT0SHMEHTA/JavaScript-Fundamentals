// Variables and Values

// 1. Variable (like a container/box)

// A variable is a named storage location in programming where we can store data (information).

// Think of it like a box with a label on it.

// The label (name of the variable) helps us find or use the data later.

// Example:

let age = 25;

// Here:

// age - is the variable (the name of the box).

// 25 - is the value stored inside the box.

// 2. Value (the actual data inside the container)

// A value is the actual piece of information that we put inside the variable.

// Values can be numbers, text (strings), true/false (booleans), objects, arrays, etc.

// Example:

let name = 'Ashutosh';

// Here:

// name - variable

// "Ashutosh" - value stored in it

// Key Points (Simple Words)

// 1. Variable = Name tag / box

// Used to store and reuse data.

// 2. Value = Data itself

// The actual thing stored inside the box.

// 3. Variables make coding easier

// Instead of writing the same value again and again, we store it in a variable and use it whenever needed.

// Real-Life Analogy: School Bag

// Think of a variable as a bag with a name tag (e.g., “Maths Book Bag”).

// The value is the actual book kept inside the bag.

// You can open the bag (variable) anytime to check or replace the book (value).

// Example:

// Variable: mathBook

// Value inside it: "Algebra Book"

// Later, you can replace the value with "Geometry Book" without changing the variable name.
