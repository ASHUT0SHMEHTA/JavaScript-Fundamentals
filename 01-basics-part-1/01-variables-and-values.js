// .1 Variable -

// A variable is a named storage in memory that holds a reference to some data. It acts like a container or label, allowing us to store, retrieve, and update values during program execution.

// 2.Value -

// A value is the actual piece of data stored inside a variable. It can be of different types such as number, string, boolean, object, etc. The value is what the program works with.

// Example:

let score = 95;
let name = "Ashutosh";

// score and name → variables (the containers / labels)

// 95 and "Ashutosh" → values (the actual data inside them)