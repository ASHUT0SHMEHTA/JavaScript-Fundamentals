// What is a Variable in JavaScript?

// A variable is a container used to store data in a program.
// It allows us to save information and use it later.

// Example:

let name = "Rahul"; // 'name' is a variable

// What are Values in JavaScript?

// A value is the actual data stored inside a variable.
// Values can be numbers, text (strings), true/false, objects, etc.

// Example:

let userName = "Rahul"; // the value is "Rahul"
let age = 25; // the value is 25
